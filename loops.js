//for loop
for (let i=0; i<5; i++) {
    console.log("Hello");
}

//while loop
let i = 1;
while (i <= 5) {
    console.log(i);
    i++;
}

//do while loop
do{
    console.log(i);
    i++;
} while (i <= 10)

//for of loop or for in loop
let str = "Tony Stark"
for (let i of str) {
    console.log(i);
}