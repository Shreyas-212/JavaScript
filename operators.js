//Arithmetic Operators
let a = 5;
let b = 10;

console.log("a is ", a, " b is ", b);
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);

//Modulus Operator
console.log(" a % b = ", a % b);

//Exponentiation Operator
console.log("a ** b = ", a ** b);

//Unary Operators - Increment and Decrement 
console.log("a is ", a, " b is ", b);
console.log("a = ", a++);
console.log("b = ", b--);

//Assignment Operators
console.log("a + 4 = ", a += 4);
console.log("a - 4 = ", a -= 4);
console.log("a * 4 = ", a *= 4);
console.log("a / 4 = ", a /= 4);
console.log("a % 4 = ", a %= 4);
console.log("a ** 4 = ", a **= 4);

//Comparison Operators - ==, !=, ===, !==, >, >=, <, <=
console.log("a == b ", a == b);
console.log("a != b ", a != b);

//Logical Operators - &&, ||, !
